USE grupos;

CREATE TABLE ZONAS (
ID INT NOT NULL,
DIR_COD INT NOT NULL,
DIR VARCHAR(20),
NOMBRE VARCHAR(20)
);

DESC ZONAS;

ALTER TABLE ZONAS
ADD PRIMARY KEY (ID);

DESC ZONAS;

CREATE TABLE GRUPO_1 (

ID INT NOT NULL,
NOMBRE VARCHAR(20),
DIR_COD INT NOT NULL,
PRIMARY KEY (ID)
);

DESC GRUPO_1;

DESC ZONAS;

ALTER TABLE GRUPO_1
ADD CONSTRAINT FK_ZONASID
foreign key (DIR_COD) references ZONAS (ID);

DESC GRUPO_1;

CREATE TABLE GRUPO_2 (

ID INT NOT NULL,
NOMBRE VARCHAR(20),
DIR_COD INT NOT NULL,
PRIMARY KEY (ID),
FOREIGN KEY (DIR_COD) REFERENCES ZONAS(ID)
